#Extracting fields of a roll number using string indexing and slicing.

Name="Hello Python Programming"

print("Index(0):     ",Name[0])
print("Index(0:3):   ",Name[0:4])
print("Index(6:11):  ",Name[2:-1])
print("Index(-7:-1): ",Name[7:-1])
print("Index(9):     ",Name[8])
print("Index(0:-1):  ",Name[0:-1])
print("Index(1:-3):  ",Name[1:-3])
print("Index(7:10):  ",Name[7:10])
print("Index(-7:-1): ",Name[-7:-3])
